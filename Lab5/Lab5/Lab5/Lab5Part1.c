//function that adds 4 to each digit on the user inputted number
int add4(int num){
    int newAddNumber=0;
    int num1=num;
    
    //goes through each of the digits of the function
    for(int i=0;num/pow(10,i)>=1;i++){
        
        //attains indvidual digits starting from last digit to first digit.
        int add4Digit=(num1%10)+4;
        
        //adds 4 to each of the digits of the inputted value. Then transfers them to orginal location by doing multpying by place value and taking total sum of all digits
        if((add4Digit<10)){
            newAddNumber=newAddNumber+add4Digit*pow(10,i);
        }
        else{
            newAddNumber=newAddNumber+(add4Digit-10)*pow(10,i);
            
            
        }
        //after accounting for a digit "remove them from the orginal number so that I can use the next digit.
        num1=num1/10;
    }
    
    return newAddNumber;
}

//function to shift the digits to the left
int shift(int num)
{   
    int newShiftNumber=0;
    int num1=num;
    
    //goes through all the digits in the number
    for(int i=0;num/pow(10,i)>=1;i++){
        
        //attains digits starting from last to first.
        int shiftLeft=(num1%10);
        
        //checks if the digit is the first digit of the number
        if((num/pow(10,i)>=10)){
            //if its not the first digit of the number, multiply the digit by the orginal place value times 10 to shift it to the left. Then sum them up
            newShiftNumber=newShiftNumber+shiftLeft*pow(10,i+1);
        }
        else{
            
            //if the digit is the first digit of the number, change the place value of the digit by multiplying the individual digit by one. and adding to the sum
            newShiftNumber=newShiftNumber+shiftLeft*1;
            
            
        }
        //"removes" the digit used earlier so you can deal with the next digit
        num1=num1/10;
        
        
 
    }
    return newShiftNumber;
}

